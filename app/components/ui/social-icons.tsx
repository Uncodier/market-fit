// Social icon component that selects the appropriate icon based on platform name
interface SocialIconProps extends IconProps {
  platform: string;
}

export function SocialIcon({ platform, ...props }: SocialIconProps) {
  const platformName = (platform || '').toLowerCase().trim();
  
  console.log("SocialIcon component called with platform:", platform, "normalized to:", platformName);
  
  switch (platformName) {
    case 'facebook':
      return <FacebookIcon {...props} />;
    case 'twitter':
    case 'x':
      return <TwitterIcon {...props} />;
    case 'instagram':
      return <InstagramIcon {...props} />;
    case 'linkedin':
      return <LinkedInIcon {...props} />;
    case 'youtube':
      return <YouTubeIcon {...props} />;
    case 'tiktok':
      return <TikTokIcon {...props} />;
    case 'pinterest':
      return <PinterestIcon {...props} />;
    case 'github':
      return <GitHubIcon {...props} />;
    case 'reddit':
      return <RedditIcon {...props} />;
    case 'medium':
      return <MediumIcon {...props} />;
    case 'whatsapp':
      return <WhatsAppIcon {...props} />;
    case 'telegram':
      return <TelegramIcon {...props} />;
    case 'discord':
      return <DiscordIcon {...props} />;
    default:
      console.log("No matching case for platform in SocialIcon:", platformName, "fallback to GlobeIcon");
      return <GlobeIcon {...props} />;
  }
} 